            rowsInDB = rst.getInt("NumOfRows");
            noOfStudentsLabel.setText(Integer.toString(rowsInDB));

            // getting logged in user form db
            sql = "SELECT `userLoggedIn` FROM `masterdata`;";
            pst = conn.prepareStatement(sql);
            rst = pst.executeQuery();
            rst.next();
            Username = rst.getString("userLoggedIn");
            welcomeNoteUsername.setText("Welcome ! " + Username);

            // GETTING TOTAL NUMBER OF BOOKS ISSUED TILL NOW.
            sql = "SELECT COUNT(1) as NumOfRows  FROM `issuedBooks`;";
            pst = conn.prepareStatement(sql);
            rst = pst.executeQuery();
            rst.next();
            rowsInDB = rst.getInt("NumOfRows");
            noOfIssuedBooksLabel.setText(Integer.toString(rowsInDB));

            // GETTING TOTAL NUMBER OF DEFAULTERS.
//            sql = "Select  COUNT(1) as NumOfRows from `issuedBooks` where `defaultDate` < 'GETDATE()' ;";
