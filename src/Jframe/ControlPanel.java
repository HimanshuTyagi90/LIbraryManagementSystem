    private void backUpDataLabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_backUpDataLabelMouseClicked

        String path = null;
        Process p = null;
        String filename = "";

        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);  //jLabel6
        String date = new SimpleDateFormat("YYYY-MM-DD").format(new Date());

        try {

            File f = fc.getSelectedFile();
            path = f.getAbsolutePath();
            path = path.replace('\\', '/');
            path = path + "_" + date + ".sql";

            Runtime runtime = Runtime.getRuntime();
            p = runtime.exec("C://xampp//mysql//bin//mysqldump.exe -Uroot  --add-drop-database -B lms -R" + path);

            int processComplete = p.waitFor();
            if (processComplete == 0) {
                JOptionPane.showMessageDialog(this, " BackUp Created Successfully !", "BackUP Creation Result", HEIGHT);
            } else {
                JOptionPane.showMessageDialog(this, "Can't Create BackUp", "BackUP Creation Result", HEIGHT);
            }
        } catch (Exception e) {

            e.printStackTrace();
        }

    }//GEN-LAST:event_backUpDataLabelMouseClicked

